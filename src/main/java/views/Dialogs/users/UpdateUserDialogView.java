    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String email, name, cpf;
        email = input_email.getText();
        name = input_name.getText();
        cpf = input_cpf.getText();
        
        UserDTO new_user = new UserDTO();
        new_user.setEmail(email);
        new_user.setName(name);
        new_user.setCpf(cpf);
        new_user.setId(user.getId());
        
        UserService services = new UserService();
        try {
            if (!email.equals(user.getEmail())){
                boolean result = services.validUSerEmail(email);
                if (result) {
                    JOptionPane.showMessageDialog(null, "Este E-mail j치 Existe, digite um E-mail diferente !");
                }else {
                    services.updateUser(new_user);
                    JOptionPane.showMessageDialog(null, "Usu치rio Alterado com Sucesso");
                    dispose();
                }
            } else {
                services.updateUser(new_user);
                JOptionPane.showMessageDialog(null, "Usu치rio Alterado com Sucesso");
                dispose();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

